    private void btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn1ActionPerformed
        // Get angka dari text field
        String jumlahAngka = txtAngka1.getText();
        
        // Jika angka nya = 0
        if(jumlahAngka.equals("0")){
            // Angka 0 diubah menjadi angka 1
            String totalAngka = "1";
            txtAngka1.setText(totalAngka);
        }
        else if(jumlahAngka.isEmpty()){
            // Angka menjadi 1
            String totalAngka = "1";
            txtAngka1.setText(totalAngka);
        }
        // Jika text1 diakhiri dengan tanda +
        else if(jumlahAngka.endsWith("+")){
            // ambil nilai txtAngka2
            String angka2 = txtAngka2.getText();
            String totalAngka = "1";
            
            // jika angka2 adalah 0
            if(angka2.equals("0")){
                // Angka 0 diubah menjadi angka 1
                txtAngka2.setText(totalAngka);
            }else{
                // jika angka 0 sudah diubah menjadi 1, maka menambahkan angka 1 terus menerus
                txtAngka2.setText(angka2 + totalAngka);
            }
        }
        // Jika angka bukan 0
        else{
          // Menambahkan jumlah angka 1 di belakang angka terakhir
          String totalAngka = jumlahAngka + "1";
          txtAngka1.setText(totalAngka);
        }
    }//GEN-LAST:event_btn1ActionPerformed

