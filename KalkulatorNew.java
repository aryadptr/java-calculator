    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // deklarasi variabel txtAngka1 dan txtAngka2
        String jumlahAngka = txtAngka1.getText();
        String jumlahAngka2 = txtAngka2.getText();
        // mengambil jumlah karakter dari variabel jumlahAngka
        int totalChar = jumlahAngka.length();
        // membuat variabel untuk menghapus karakter terakhir
        String delete1 = removeLastChar(jumlahAngka);
        String delete2 = removeLastChar(jumlahAngka2);
        
        // jika jumlah angka nya = 0
        if(jumlahAngka.equals("0")){
            // Angka 0 diubah menjadi angka 1
            String totalAngka = "0";
            txtAngka1.setText(totalAngka);
        }else if(jumlahAngka.isEmpty()){
            // Angka 0 diubah menjadi angka 1
            String totalAngka = "0";
            txtAngka1.setText(totalAngka);
        }
        // jika txtAngka1 nilai nya = 1
        else if(totalChar == 1){
            String totalAngka = "0";
            txtAngka1.setText(totalAngka);
        }
        // jika txt angka 2 ada isi nya
        else if(txtAngka2.getText().length() >= 1)
        {
            // maka yang dihapus adalah charakter dari txtAngka2
            txtAngka2.setText(delete2);
        }
        // Jika txtAngka1 ada isi nya dan belum meng-klik operator
        else{
          // Maka yang dihapus adalah karakter txtAngka1
          txtAngka1.setText(delete1);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

